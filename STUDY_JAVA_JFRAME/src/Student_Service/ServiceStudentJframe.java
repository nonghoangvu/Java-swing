    public void fillTable() {
        DefaultTableModel tblModel = (DefaultTableModel) tblStudent.getModel();
        tblModel.setRowCount(0);
        for (Student student : this.studentService.getList()) {
            String gender = (student.getGender() == 0) ? "Nữ" : "Nam";
            String status = student.getStatus() ? "Studying" : "Stopping";
            Object[] row = { student.getName(), student.getId(), student.getEmail(), gender, status,
                    student.getMajor() };
            tblModel.addRow(row);
        }
    }

