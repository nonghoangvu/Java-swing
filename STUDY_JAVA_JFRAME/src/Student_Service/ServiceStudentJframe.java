    private void btnReadActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnReadActionPerformed
        File file = new File(fileName);
        if (!file.exists()) {
            JOptionPane.showMessageDialog(this, "File not found");
            return;
        }
        try {
            FileInputStream fis = new FileInputStream(file);
            ObjectInputStream ois = new ObjectInputStream(fis);
            ArrayList<Student> list = (ArrayList<Student>) ois.readObject();
            ois.close();
            this.studentService.setList(list);
            fillTable();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }// GEN-LAST:event_btnReadActionPerformed

    private void btnWriteActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnWriteActionPerformed
        ArrayList<Student> list = this.studentService.getList();
        File file = new File(fileName);
        if (!file.exists()) {
            JOptionPane.showMessageDialog(this, "File not found");
            return;
        }
        try {
            FileOutputStream fos = new FileOutputStream(file);
            ObjectOutputStream oos = new ObjectOutputStream(fos);
            oos.writeObject(list);
            oos.close();
            JOptionPane.showMessageDialog(this, "Success");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }// GEN-LAST:event_btnWriteActionPerformed

    private void tblStudentMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_tblStudentMouseClicked
        int index = tblStudent.getSelectedRow();
        Student std = this.studentService.getList().get(index);
        txtName.setText(std.getName());
        txtId.setText(std.getId());
        txtEmail.setText(std.getEmail());
        txtPw.setText(std.getPassword());
        if (std.getGender() == 0) {
            rdoFemale.setSelected(true);
        } else {
            rdoMale.setSelected(true);
        }
        ckbStatus.setSelected(std.getStatus());
        cbbMajor.setSelectedItem(std.getMajor());

    }// GEN-LAST:event_tblStudentMouseClicked

    private void btnChangeActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnChangeActionPerformed
        int index = tblStudent.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dữ liệu cần sửa", "Thông báo",
                    JOptionPane.INFORMATION_MESSAGE);
            return;

        }
        String name = txtName.getText();
        String id = txtId.getText();
        String email = txtEmail.getText();
        String password = String.valueOf(txtPw.getPassword());
        int gender = rdoFemale.isSelected() ? 0 : 1;
        Boolean status = ckbStatus.isSelected();
        String major = (String) cbbMajor.getSelectedItem();
        this.studentService.change(index, new Student(name, id, email, password, gender, status, major));
        fillTable();
        clear();
    }// GEN-LAST:event_btnChangeActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnDeleteActionPerformed
        int index = tblStudent.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dữ liệu cần xóa", "Thông báo",
                    JOptionPane.INFORMATION_MESSAGE);
            return;

        }
        this.studentService.remove(index);
        fillTable();
        clear();
    }// GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnAddActionPerformed
        int gender = rdoFemale.isSelected() ? 0 : 1;
        if (txtName.getText().isBlank() || txtId.getText().isBlank() || txtEmail.getText().isBlank()
                || String.valueOf(txtPw.getPassword()).isBlank()) {
            JOptionPane.showMessageDialog(this, "Bạn chưa thêm dữ liệu", "Thông báo",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        this.studentService.insert(
                new Student(txtName.getText(), txtId.getText(), txtEmail.getText(),
                        String.valueOf(txtPw.getPassword()),
                        gender, ckbStatus.isSelected(), (String) cbbMajor.getSelectedItem()));
        fillTable();
        clear();
    }// GEN-LAST:event_btnAddActionPerformed

