        private void btnChangeActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnChangeActionPerformed
                DefaultTableModel tblModel = (DefaultTableModel) tblStudent.getModel();
                String name = txtName.getText();
                String id = txtStudentID.getText();
                String email = txtEmail.getText();

                tblModel.setValueAt(name, tblStudent.getSelectedRow(), 0);
                tblModel.setValueAt(id, tblStudent.getSelectedRow(), 1);
                tblModel.setValueAt(email, tblStudent.getSelectedRow(), 2);

                // Show message display
                JOptionPane.showMessageDialog(this, "Update success", "Update", JOptionPane.INFORMATION_MESSAGE);
        }// GEN-LAST:event_btnChangeActionPerformed

        private void tblStudentMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_tblStudentMouseClicked
                DefaultTableModel tblModel = (DefaultTableModel) tblStudent.getModel();
                // Set data to text field when raw is selected
                String tblName = tblModel.getValueAt(this.tblStudent.getSelectedRow(), 0).toString();
                String tblId = tblModel.getValueAt(this.tblStudent.getSelectedRow(), 1).toString();
                String tblEmail = tblModel.getValueAt(this.tblStudent.getSelectedRow(), 2).toString();

                // Set to text field
                txtName.setText(tblName);
                txtStudentID.setText(tblId);
                txtEmail.setText(tblEmail);
        }// GEN-LAST:event_tblStudentMouseClicked

        private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnClearActionPerformed
                clear();
        }// GEN-LAST:event_btnClearActionPerformed

        private void btnRemoveMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_btnRemoveMouseClicked
                DefaultTableModel tblModel = (DefaultTableModel) tblStudent.getModel();
                tblModel.removeRow(this.tblStudent.getSelectedRow());
        }// GEN-LAST:event_btnRemoveMouseClicked

        public void clear() {
                txtName.setText("");
                txtStudentID.setText("");
                txtEmail.setText("");
                txtPw.setText("");
                rdoFemale.setSelected(true);
                cbStatus.setSelected(false);
                cboMajor.setSelectedIndex(0);
        }

        private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnAddActionPerformed
                // Add row data
                String gender = rdoFemale.isSelected() ? "Nu" : "Nam";
                String status = cbStatus.isSelected() ? "Studying" : "Stopping";
                String selectMajor = (String) cboMajor.getSelectedItem();

                String[] rowData = { txtName.getText(), txtStudentID.getText(), txtEmail.getText(), gender, status,
                                selectMajor };
                DefaultTableModel tblModel = (DefaultTableModel) tblStudent.getModel();
                tblModel.addRow(rowData);
                clear();
        }// GEN-LAST:event_btnAddActionPerformed

