        public void fillTable() {
                DefaultTableModel tblModel = (DefaultTableModel) tblStudent.getModel();
                tblModel.setRowCount(0);
                this.std_sv = new STUDENT_SERVICE();
                for (Student std : this.std_sv.getList()) {
                        DefaultTableModel tblModel_ = (DefaultTableModel) tblStudent.getModel();
                        String gender = (std.getGender() == 0) ? "Nữ" : "Nam";
                        String status = (std.getStatus() == 0) ? "Stopping" : "Studying";
                        Object[] row = { std.getName(), std.getId(), std.getEmail(), gender, status, std.getMajor() };
                        tblModel_.addRow(row);
                }
        }

